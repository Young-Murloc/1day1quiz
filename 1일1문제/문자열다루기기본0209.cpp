/*
문자열 s의 길이가 4 혹은 6이고, 숫자로만 구성돼있는지 확인해주는 함수, solution을 완성하세요. 예를 들어 s가 a234이면 False를 리턴하고 1234라면 True를 리턴하면 됩니다.
*/

#include <string>
#include <vector>
#include <iostream>
#include <algorithm>

using namespace std;

bool solution(string s) {
    bool answer = true;

    if (s.length() == 4 || s.length() == 6) {
        for (int i = 0; i < s.size(); i++) {
            if (int(s[i]) < 48 || int(s[i]) > 57) {
                answer = false;
                break;
            }
        }
    }
    else {
        answer = false;
    }

    return answer;
}